package com.smsanalytic.lotto.model.sms;

public class LotoHitDetail {
    private String tinGoc;
    private String tinSauXuLy;
    private String chiTiet;
    private String title;

    public String getTinGoc() {
        return tinGoc;
    }

    public void setTinGoc(String tinGoc) {
        this.tinGoc = tinGoc;
    }

    public String getTinSauXuLy() {
        return tinSauXuLy;
    }

    public void setTinSauXuLy(String tinSauXuLy) {
        this.tinSauXuLy = tinSauXuLy;
    }

    public String getChiTiet() {
        return chiTiet;
    }

    public void setChiTiet(String chiTiet) {
        this.chiTiet = chiTiet;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
